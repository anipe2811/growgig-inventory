/**
 * Return the current active language code ('en' or 'ms').
 */
function current_lang(): string
{
    $lang = $_SESSION['lang'] ?? 'en';
    return in_array($lang, ['en', 'ms'], true) ? $lang : 'en';
}

/**
 * Translate a key for the current language.
 * Returns the string (print with <?= __('key') ?>).
 */
function __($key): string
{
    global $LANG;
    $lang = current_lang();
    return $LANG[$lang][$key] ?? ($LANG['en'][$key] ?? $key);
}

/**
 * Convenience helper to translate a role code into its localized label.
 */
function role_label(?string $role): string
{
    return $role ? __('role_' . $role) : '';
}
